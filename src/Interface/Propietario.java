    private boolean actualizarTabla(){
        boolean tablaActualizada = true;
        Metodos.Querys query = new Metodos.Querys();
        ResultSet resultSetRegistros;
        String txtQuery="idVehiculo , ";
        int i;
        
        for(i=1; i<(caracteristicasRegistros.size()-1);i++){
            txtQuery+= caracteristicasRegistros.get(i).toString() + " , ";
        }
        txtQuery+= caracteristicasRegistros.get(i).toString();
        
        resultSetRegistros= query.Select(txtQuery
                ,"idUsuario = '"+ propietarioUser.getIdUsuario()+"'"
                , "vehiculos");
        
        DefaultTableModel model = new DefaultTableModel();
        
        for(i=0; i<caracteristicasRegistros.size();i++){
            model.addColumn(caracteristicasRegistros.get(i).toString());
        }
        
        try {
            while (resultSetRegistros.next()){
                int cols = resultSetRegistros.getMetaData().getColumnCount();
                // Se crea un array que ser√° una de las filas de la tabla.
                Object [] fila = new Object[cols]; // Se ingresa el numero total de columnas necesitadas
                // Se rellena cada posici√≥n del array con una de las columnas de la tabla en base de datos.
                for ( i=0 ; i<cols;i++)
                    fila[i] = resultSetRegistros.getObject(i+1); // El primer indice en rs es el 1, no el cero, por eso se suma 1.
                // Se a√±ade al modelo la fila completa.
                model.addRow(fila);
                tablaActualizada = true;
            }
            this.jTable2.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(PanelControl.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return tablaActualizada;
    }
