    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        Metodos.Avisos avisos = new Metodos.Avisos();
        String Modelo, Marca, Placas, Color, Serie, Motor;
        int Ano = vehiculoRegistro.getAno(), TarjCirculacion = vehiculoRegistro.getTarjCirculacion();

        if( this.btnModificar.getText().equals("Modificar")){
            System.out.print("Entra Guardar");
            if(adminUser.isAdmin()){
                cambiarEstadoDeEditables(adminUser.confirmacionSeguridad(5));
            }else
                JOptionPane.showMessageDialog(null, "Usted no es administrador"
                        , "Problemas de autorizacion", JOptionPane.ERROR_MESSAGE);
        } else if( this.btnModificar.getText().equals("Guardar cambios") ) {
            
            System.out.print("Entra Guardar");
            if(avisos.Pregunta("seguro", "continuar")==JOptionPane.YES_OPTION){

                Modelo = txtModelo.getText();
                Marca = txtMarca.getText();
                Placas = txtPlacas.getText();
                Color = txtColor.getText();
                Serie = txtSerie.getText();
                Motor = txtMotor.getText();
                TarjCirculacion = Integer.parseInt(txtTarjCirculacion.getText());
                Ano = Integer.parseInt((String) cmbxAno.getSelectedItem());

                if(!Modelo.equals(vehiculoRegistro.getModelo()) && !Modelo.isEmpty()){
                    vehiculoRegistro.setModelo(Modelo);
                }
                if(!Marca.equals(vehiculoRegistro.getMarca()) && !Marca.isEmpty()){
                    vehiculoRegistro.setMarca(Marca);
                }
                if(!Placas.equals(vehiculoRegistro.getPlacas()) && !Placas.isEmpty()){
                    vehiculoRegistro.setPlacas(Placas);
                }
                if(!Color.equals(vehiculoRegistro.getColor()) && !Color.isEmpty()){
                    vehiculoRegistro.setColor(Color);
                }
                if(!Serie.equals(vehiculoRegistro.getSerie()) && !Serie.isEmpty()){
                    vehiculoRegistro.setSerie(Serie);
                }
                if(!Motor.equals(vehiculoRegistro.getMotor()) && !Motor.isEmpty()){
                    vehiculoRegistro.setMotor(Motor);
                }
                if(TarjCirculacion !=0 && TarjCirculacion != vehiculoRegistro.getTarjCirculacion()){
                    vehiculoRegistro.setTarjCirculacion(TarjCirculacion);
                }
                if(Ano != vehiculoRegistro.getAno()){
                    vehiculoRegistro.setAno(Ano);
                }
                cambiarEstadoDeEditables(!vehiculoRegistro.checkVehiculo());
            }
        } else
            System.out.print("No Entra a Modifcar ni Guardar");
    }//GEN-LAST:event_btnModificarActionPerformed

