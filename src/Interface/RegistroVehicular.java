    private void isTypedInt(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_isTypedInt
        String isNumber = txtTarjCirculacion.getText();
        int number=0;
        if(isNumber.length()<=10){
            try
            {
                number = Integer.parseInt(isNumber);
            } catch (NumberFormatException ex){
                txtTarjCirculacion.setText(isNumber.substring(0, (isNumber.length()-1)));
                new Metodos.Avisos("ingresar solo letras","Este campo solo puede ser llenado con numeros");
            }
        } else
            txtTarjCirculacion.setText(isNumber.substring(0, (isNumber.length()-2)));
    }//GEN-LAST:event_isTypedInt

    private void cambiarEstadoDeEditables(boolean status){
        txtModelo.setEnabled(status);
        cmbxAno.setEnabled(status);
        txtMarca.setEnabled(status);
        txtPlacas.setEnabled(status);
        txtColor.setEnabled(status);
        txtSerie.setEnabled(status);
        txtMotor.setEnabled(status);
        txtTarjCirculacion.setEnabled(status);      
        if(status)this.btnModificar.setText("Guardar cambios");
        else{
            txtModelo.setText(vehiculoRegistro.getModelo());
            cmbxAno.setSelectedItem(""+vehiculoRegistro.getAno());
            txtMarca.setText(vehiculoRegistro.getMarca());
            txtPlacas.setText(vehiculoRegistro.getPlacas());
            txtColor.setText(vehiculoRegistro.getColor());
            txtSerie.setText(vehiculoRegistro.getSerie());
            txtMotor.setText(vehiculoRegistro.getMotor());
            txtTarjCirculacion.setText(vehiculoRegistro.getTarjCirculacion()+"");
            this.btnModificar.setText("Modificar");
        }
    }
