    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String email = campoEmail.getText();
        String password = String.valueOf(campoPassword.getPassword());
        Metodos.User newUser;
        PanelControl adminInterface;
        Propietario userInterface;
        Avisos Aviso;
        Validador Validar = new Validador();
        
        this.textPassword.setForeground(Color.red);
        this.textEmail.setForeground(Color.BLACK);
        
        if (email.isEmpty()){
            
            Aviso = new Avisos("login", "datosFaltantes");
            this.textEmail.setForeground(Color.red);
        }else if(!Validar.emailPattner(email)){ 
            
            Aviso = new Avisos("email", "incorrecto");
            this.textEmail.setForeground(Color.red);
        }else{
            
            if (password.isEmpty())
                Aviso = new Avisos("login", "datosFaltantes");
            else{
                this.textPassword.setForeground(Color.BLACK);
                newUser = new Metodos.User(email, password);
                if (newUser.checkUser()) {
                    if(newUser.isAdmin()){
                        
                        Aviso = new Avisos("login", "exitoLogin");
                        PanelControl.main(newUser);
                        this.setVisible(false);
                    }else{
                        
                        Aviso = new Avisos("login", "exitoLogin");
                        Propietario.main(newUser, newUser);
                        this.setVisible(false);
                    }
                }else{
                    Aviso = new Avisos("login", "errorLogin");
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

